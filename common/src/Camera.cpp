mat4 Camera::GetViewMatrix()
{
  //  LogCamera();
    static unsigned long slowIterator = 0;
    if (g_CameraPosition[g_CameraIteratorIndex][0] == 1)
    {
        m_Position.r = g_CameraPosition[g_CameraIteratorIndex][1];
        m_Position.g = g_CameraPosition[g_CameraIteratorIndex][2];
        m_Position.b = g_CameraPosition[g_CameraIteratorIndex][3];
    }
    else
    {
        m_viewVector.r = g_CameraPosition[g_CameraIteratorIndex][1];
        m_viewVector.g = g_CameraPosition[g_CameraIteratorIndex][2];
        m_viewVector.b = g_CameraPosition[g_CameraIteratorIndex][3];
    }
    if ((slowIterator == 29))
    {
        slowIterator = 0;
        g_CameraIteratorIndex++;
    }
    slowIterator++;
    return glm::lookAt(m_Position, m_Position + m_viewVector, m_upVector);
}

void Camera::LogCamera()
{
    static float oldVal;
    if (oldVal == m_Position.r)
    {
        return;
    }
    FILE* fptr = NULL;
    fopen_s(&fptr, "CameraPosition.txt", "a+");
    if (fptr)
    {
        oldVal = m_Position.r;
        fprintf(fptr, "# %f %f %f \n", m_Position.r, m_Position.g, m_Position.b);
        fclose(fptr);
        fptr = NULL;
    }
    return;
}

